package com.desu.experiments.viewModel.item;

public class ItemBookmarkViewModel {
    String textViewTitle;
    String textViewDomain;
    String textViewDateSaved;
    String textViewFullUrl;

    public String getTextViewTitle() {
        return textViewTitle;
    }
    public void setTextViewTitle(String textViewTitle) {
        this.textViewTitle = textViewTitle;
    }
    public String getTextViewDomain() {
        return textViewDomain;
    }
    public void setTextViewDomain(String textViewDomain) {
        this.textViewDomain = textViewDomain;
    }
    public String getTextViewDateSaved() {
        return textViewDateSaved;
    }
    public void setTextViewDateSaved(String textViewDateSaved) {
        this.textViewDateSaved = textViewDateSaved;
    }
    public String getTextViewFullUrl() {
        return textViewFullUrl;
    }
    public void setTextViewFullUrl(String textViewFullUrl) {
        this.textViewFullUrl = textViewFullUrl;
    }
}
